unsigned char nonce[24] = {0};

unsigned char m[BUFSIZE + crypto_box_ZEROBYTES] = {0};

unsigned char c[BUFSIZE + crypto_box_ZEROBYTES];

int main(int argc, char *argv[])
{
  FILE *fp;

  fp =  fopen(argv[1], "r");
  if (!fp)
    {
      printf ("Khong mo duoc file!\n");
      return 1;
    }

  FILE *fpout;

  fpout =  fopen(argv[2], "w+");
  if (!fpout)
    {
      printf ("Khong mo duoc file de ghi!\n");
      return 1;
    }


  int mlen;
  
  unsigned char k[crypto_box_BEFORENMBYTES];

  
  crypto_box_beforenm(k, bobpk, alicesk);

  randombytes(nonce, crypto_box_NONCEBYTES);
  
  //write2file = signatureAlicepk | nonce | c
  
  int len = fwrite (signatureAlicepk, 1, 32 + crypto_sign_BYTES, fpout);
  if (len != 32 + crypto_sign_BYTES)
    {
      printf ("Loi ghi file!\n");
      return 1;
    }	 

  len = fwrite (nonce, 1, crypto_box_NONCEBYTES, fpout);
  if (len != crypto_box_NONCEBYTES)
    {
      printf ("Loi ghi file!\n");
      return 1;
    }
  
  printf("Crypto_box_ZEROBYTES = %ld\n " ,crypto_box_ZEROBYTES);
  while (mlen= fread(m+crypto_box_ZEROBYTES, 1, BUFSIZE, fp))
    {
      mlen += crypto_box_ZEROBYTES;
      crypto_box_afternm(c,m,mlen,nonce,k);
      fwrite(c+crypto_box_BOXZEROBYTES, 1, mlen - crypto_box_BOXZEROBYTES, fpout);
      sodium_increment(nonce, crypto_box_NONCEBYTES);  // Nen thay doi de khong phu thuoc vao sodium
    }

  fclose(fp);
  fclose (fpout);

  return 0;
}
